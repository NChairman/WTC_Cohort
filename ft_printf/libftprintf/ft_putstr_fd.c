#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	char	*s1;
	size_t	i;

	if (s)
	{
		s1 = (char *)s;
		i = 0;
		while (s1[i] != '\0')
			write(fd, s1 + i++, 1);
	}
}
