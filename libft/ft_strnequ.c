#include "libft.h"
#include <stdio.h>
#include <string.h>

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (n == 0)
		return (1);
	if (s1 && s2)
		if ((ft_strncmp(s1, s2, n) == 0) && (s1[n - 1] == s1[n - 1]))
			return (1);
	return (0);
}
