#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	if (s1 && s2)
		if (ft_strcmp(s1, s2) == 0 && ft_strlen(s1) == ft_strlen(s2))
			return (1);
	return (0);
}
